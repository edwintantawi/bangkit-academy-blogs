function formatDate(date) {
  return new Intl.DateTimeFormat('id-ID', {
    dateStyle: 'long',
    timeZone: 'Asia/Jakarta',
  }).format(date);
}

function createArticle(article) {
  return `
    <article class="article">
        <header>
            <img
                loading="lazy"
                src="${article.image.source}"
                alt="${article.image.title}"
                class="article__image"
            />
            <time datetime="${article.date}" class="article__date">
                ${formatDate(new Date(article.date))}
            </time>
            <h2 class="article__title">
                <a href="${article.url}" target="_blank" class="article__link">
                    ${article.title}
                </a>
            </h2>
        </header>
        <p class="article__summary">
            ${article.summary}
        </p>
    </article>
    `;
}

function renderArticles(articles) {
  const articleContainer = document.getElementById('article-container');
  const articleTemplates = [];
  for (const article of articles) {
    articleTemplates.push(createArticle(article));
  }
  articleContainer.innerHTML = articleTemplates.join('');
}

renderArticles(articles);
